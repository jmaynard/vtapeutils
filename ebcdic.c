void ebcdic_to_ascii(unsigned char *inbuffer, unsigned char *outbuffer,
  int maxcount)
{
  int index;
  
  /* Simple translation loop. */
  for (index=0; index<maxcount && inbuffer[index] != '\0'; index++)
    outbuffer[index] = ascii_table[inbuffer[index]];
}

void ascii_to_ebcdic(unsigned char *inbuffer, unsigned char *outbuffer,
  int maxcount)
{
  int index;
  
  /* Simple translation loop. */
  for (index=0; index<maxcount && inbuffer[index] != '\0'; index++)
    outbuffer[index] = ebcdic_table[inbuffer[index]];
}
